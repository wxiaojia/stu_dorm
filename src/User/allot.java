package User;

public class allot {
	private int room,ida,idb,idc,idd;
	public allot(){
		
	}
	public allot(int room, int ida, int idb, int idc, int idd) {
		//super();
		this.room = room;
		this.ida = ida;
		this.idb = idb;
		this.idc = idc;
		this.idd = idd;
	}
	public int getRoom() {
		return room;
	}
	public void setRoom(int room) {
		this.room = room;
	}
	public int getIda() {
		return ida;
	}
	public void setIda(int ida) {
		this.ida = ida;
	}
	public int getIdb() {
		return idb;
	}
	public void setIdb(int idb) {
		this.idb = idb;
	}
	public int getIdc() {
		return idc;
	}
	public void setIdc(int idc) {
		this.idc = idc;
	}
	public int getIdd() {
		return idd;
	}
	public void setIdd(int idd) {
		this.idd = idd;
	}
	
}
